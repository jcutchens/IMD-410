                    <h3>Study Questions:</h3>
                     <h4>Week 1:</h4> 

                        <p><strong>1.  List 3 ways PHP is different in use from JavaScript.</strong></p>
                            a.  PHP = serverside language & JavaScript = local/browser language</br>
                            b.  PHP needs to be loaded on a server to test/ JS can be tested without</br>
                            c.  PHP uses a different syntax and acts more dynamically than JS.

                         <p><strong>2.  List the sequence of events triggered by viewing a PHP page.</strong></p>
                            a.  PHP gets a request from user and uses a PHP engine to get data from database and then plugs that back into the requested web page/app.
                            
						<p><strong>3.  Define the term “middleware”.</strong></p>
Programming or software that connects processes such as browser and server database together to execute exchanges of data.
 
						<p><strong>4.  Define the term “back-end”.</strong></p>
Server-side or "back-end" relates to the tasks and software that operate to complete tasks for users that are developed separately from html/css/js.

						<p><strong>5. What is meant by an n-tier web application?</strong>
A data application that is separated into tiers that are distributed between the client and the server.

						<p><strong>6.  What are “magic quotes”</strong></p>
Originally a way for beginner users of php to write code securely.  It used special characters that were prefixed with a backslash before being passed on.
</p>
						<p><strong>7.  What is revision/version control software?</strong></p>
These are applications that serve as save points for a project versus saving multiple versions of a project on your computer.  This allows users to be able to access versions of a project remotely and even revert to a prior version due to mistakes or other errors.
 
						<p><strong>8.  List the two most popular version control systems.</strong></p>
Currently the 2 most popular versions are GitHub and Subversion.

						<p><strong>9.  What is a repository?</strong></p>
A repository acts like an online folder that stores all committed versions of a project.
 
						<p><strong>10.  What are the steps in submitting changes to git?</strong></p>
Using a client such as github allows a user to create a repository and commit/submit changes to their project.

						<p><strong>11.  What is GitHub?</strong></p>
A web-based git version control platform that allows users to establish repositories for their versions of software.
 
						<p><strong>12.  Explain the differences between a local and remote repository.</strong></p>
A local repository would be similar to  folder that is saved on a user's desktop to contain all versions of a project.  A remote repository would be like a folder saved online and accessible from anywhere through a client such as GitHub.


